const RECENT_STORAGE_KEY = 'lch:emoji:recent';
/** How many "frequently used" glyphs to keep / surface (≈ two grid rows). */
const RECENT_LIMIT = 24;

/** Read the persisted "frequently used" glyphs, tolerating unavailable storage. */
export function readRecentEmojis(): string[] {
  try {
    const raw = globalThis.localStorage?.getItem(RECENT_STORAGE_KEY);
    if (!raw) return [];
    const parsed: unknown = JSON.parse(raw);
    if (!Array.isArray(parsed)) return [];
    return parsed.filter((e): e is string => typeof e === 'string').slice(0, RECENT_LIMIT);
  } catch {
    return [];
  }
}

/** Promote a just-picked glyph to the front of the recents and persist it. */
export function pushRecentEmoji(emoji: string, current: string[]): string[] {
  const next = [emoji, ...current.filter((e) => e !== emoji)].slice(0, RECENT_LIMIT);
  try {
    globalThis.localStorage?.setItem(RECENT_STORAGE_KEY, JSON.stringify(next));
  } catch {
    // Storage may be unavailable (private mode, sandboxed iframe) — ignore.
  }
  return next;
}
